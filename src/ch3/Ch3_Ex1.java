package ch3;

public class Ch3_Ex1 {

	public static void main(String[] args) {
		
		int i = 5;
		i++; // i = i + 1; 과 같은 의미
		
		System.out.println(i);
		
		i = 5; // i 값을 다시 5로 초기화
		++i;
		
		System.out.println(i);
		
		// 증감연산자가 수식이나 메서드 호출에 포함되지 않고 독립적인 하나의 문장으로 쓰인 경우에는 전위형과 후위형의 차이가 없음 
		
	}

}
